#define SOURCES_ROOT "game_src/decompiled/"

int load_sectors(FILE *file, char *psx_mem, uint32_t base_addr, uint32_t sector, uint32_t sectors)
{
  assert(base_addr >= 0x80000000 && base_addr < 0x80200000);
  uint32_t addr = base_addr - 0x80000000;

  for (int i = 0; i < sectors; i++)
  {
    uint32_t offset = (sector + i) * 0x930 + 24;
    assert(fseek(file, offset, SEEK_SET) == 0);
    offset = addr + 0x800 * i;
    int things_read = fread(psx_mem + offset, 0x800, 1, file);
    if (things_read != 1) return 1;
  }

  return 0;
}

struct exe_file {
  char *name;
  uint32_t base;
  uint32_t sector;
  uint32_t sector_num;
  struct jump_list_list jumps;
  struct entries entries;
  struct skips skips;
};

int read_from_cd(struct exe_file exe, addr_list *external_calls)
{
  char *filename = ROM_NAME;

  int err;

  FILE *file = fopen(filename, "rb");
  if (file == NULL) {
    printf("%s %m\n", filename);
    return 1;
  }

  char *psx_mem = malloc(0x00200000);

  uint32_t *psx_mem2 = (uint32_t *)psx_mem;
  for (int i = 0; i < 0x00200000/4; i++)
  {
    psx_mem2[i] = 0x007000D; // BREAK
  }

  err = load_sectors(file, psx_mem, exe.base, exe.sector, exe.sector_num);
  if (err) return err;

  err = fclose(file);
  if (err) return err;

  struct mem_range range = {
    .base = exe.base,
    .size = 0x800 * exe.sector_num
  };

  char decompname[1024];
  snprintf(decompname, sizeof(decompname), "%s%s.c", SOURCES_ROOT, exe.name);
  
  FILE *output = fopen(decompname, "w");
  if (output == NULL) return 1;

  snprintf(decompname, sizeof(decompname), "%s%s.h", SOURCES_ROOT, exe.name);
  
  FILE *header = fopen(decompname, "w");
  if (header == NULL) return 1;
  
  struct program prog = {
    .psx_mem = psx_mem,
    .range = range,
    .entries = exe.entries,
    .jumpss = exe.jumps,
    .skips = exe.skips,
    .id = exe.name,
    .output = output,
    .header = header,
    .external_calls = external_calls
  };

  err = read_instructions(prog);
  if (err) return err;

  err = fclose(output);
  if (err) return err;
  
  free(psx_mem);

  return 0;
}

